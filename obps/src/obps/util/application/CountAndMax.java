package obps.util.application;

public class CountAndMax {
    private Integer count;
    private Long max;

    public CountAndMax() {
    }

    public Integer getCount() {
        return count;
    }
    public void setCount(Integer count) {
        this.count = count;
    }

    public Long getMax() {
        return max;
    }
    public void setMax(Long max) {
        this.max = max;
    }
    
}
